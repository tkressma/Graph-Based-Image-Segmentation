public class Pixel {
    private int xCoordinate;
    private int yCoordinate;

    public Pixel (int x, int y) {
        this.xCoordinate = x;
        this.yCoordinate = y;
    }

    public int getxCoordinate() {
        return this.xCoordinate;
    }

    public int getyCoordinate() {
        return this.yCoordinate;
    }

}
